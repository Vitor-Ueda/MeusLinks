document.body.setAttribute('class', 'undefined');

let buttonDarkPink = document.querySelector('.btn-dark-pink');
let buttonDarkBlue = document.querySelector('.btn-dark-blue');
let buttonRose     = document.querySelector('.btn-rose');
let buttonWhite   = document.querySelector('.btn-white');

buttonDarkPink.addEventListener('click', () =>{
    if(document.getElementsByClassName('')){
        document.body.setAttribute('class', 'dark-pink');
    }
});

buttonDarkBlue.addEventListener('click', () =>{
    if(document.getElementsByClassName('')){
        document.body.setAttribute('class', 'dark-blue');
    }
});

buttonRose.addEventListener('click', () =>{
    if(document.getElementsByClassName('')){
        document.body.setAttribute('class', 'rose');
    }
});

buttonWhite.addEventListener('click', () =>{
    if(document.getElementsByClassName('')){
        document.body.setAttribute('class', 'undefined');
    }
});


